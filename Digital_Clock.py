from calendar import month
from tkinter import *
import datetime
def settime():
    time = datetime.datetime.now()
    hr = time.strftime('%I')
    min = time.strftime('%M')
    sec = time.strftime('%S')
    am_pm = time.strftime("%p")
    date = time.strftime("%d")
    month = time.strftime("%m")
    year = time.strftime("%y")
    day =  time.strftime("%a")
    label_hr.config(text = hr)
    label_min.config(text=min)
    label_sec.config(text=sec)
    label_am.config(text=am_pm.upper())
    label_date.config(text=date)
    label_month.config(text=month)
    label_year.config(text=year)
    label_day.config(text=day.upper())
    label_hr.after(200,settime)
